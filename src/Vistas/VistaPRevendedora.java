    public void cabeceraPedido(){
    ArrayList<Object> column = new ArrayList<>();
    column.add("ID");
    column.add("CAJAS");
    column.add("ESTADO");
    column.add("CAMPAÃ‘A");
    column.add("IMPORTE");
    column.add("ESTRELLAS");
    
    
    for(Object o:column){
    tablaPedido.addColumn(o);
    }
    tabla.setModel(tablaPedido);
    
    }
    
    public void cargarRevendedoras(){
    List<Revendedora> lista = rd.obtenerRevendedoras();
    for(Revendedora r:lista){
    cbRevendedora.addItem(r);
        }
     }
    public void borrarFilas(){
        try{
        int a = tablaPedido.getRowCount()-1;
       
        for(int i=a;i>=0;i--){
            tablaPedido.removeRow(i);
            
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(this, "La revendedora no tiene pedidos");
    }
    }
   
    
    public void cargarPedidos(){
     
    Revendedora r = (Revendedora) cbRevendedora.getSelectedItem();
    List <Pedido> lista = pd.listarPedidosRevendedora(r);
    for(Pedido p:lista){
        tablaPedido.addRow(new Object[]{p.getIdPedido(),p.getCajas(),p.isEstado(),p.getCamp(),p.getImporte(),p.getEstrellaPedido()});
    }
    
    }

